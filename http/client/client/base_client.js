import { Header } from '../header.js';

//////////////////////////////////////////////////////////////////////
// Class
//////////////////////////////////////////////////////////////////////
class BaseClient {

    //////////////////////////////////////////////////////////////////////
    // Constructor
    //////////////////////////////////////////////////////////////////////
    constructor(url) {
        this.url = url;
        this.header = new Header();
        this.body;
        this.method;
        this.timeout;
        this.isLoading = false;
    }


    //////////////////////////////////////////////////////////////////////
    // Append header.
    //////////////////////////////////////////////////////////////////////
    AppendHeader(name, value) {
        this.header.fields.push({
            name: name,
            value: value
        });
        return this;
    }


    //////////////////////////////////////////////////////////////////////
    // Is Loading.
    //////////////////////////////////////////////////////////////////////
    IsLoading() {
        this.isLoading;
    }


    //////////////////////////////////////////////////////////////////////
    // Set body.
    //////////////////////////////////////////////////////////////////////
    SetBody(body) {
        this.body = body;
        return this;
    }


    //////////////////////////////////////////////////////////////////////
    // Set timeout with milliseconds.
    //////////////////////////////////////////////////////////////////////
    SetTimeout(milliSeconds) {
        this.timeout = milliSeconds;
    }

}
export { BaseClient };
