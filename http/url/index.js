class Url {

    //////////////////////////////////////////////////////////////////////
    // Constructor
    //////////////////////////////////////////////////////////////////////
    constructor() {
        
    }


    //////////////////////////////////////////////////////////////////////
    // Get the URL query's parameter value from the key.
    //////////////////////////////////////////////////////////////////////
    static GetUrlQueryValByKey(key) {
        let result;
        window.location.href.replace(/[?&]+([^=&]+)=([^&]*)/gi, function(href, k, v) {
            if(key === k) {
                result = v;
                return;
            }
        });
        return result;
    }

}
export { Url };
